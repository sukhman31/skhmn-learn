def show_stop_words() -> list[str]:
    return STOP_WORDS 

def remove_stop_words(content: str, custom_keywords: list[str]) -> str:
    split_content = content.replace('.',' ').split(' ')
    final_content = []
    for word in split_content:
        if word in STOP_WORDS or word in custom_keywords:
            continue
        final_content.append(word) 
    return " ".join(final_content)

def lemmatize_string(content: str) -> str:
    lemmatized_content = nlp(content)
    return " ".join([token.lemma_ for token in lemmatized_content])

def only_words(content: str) -> str:
    split_content = content.split(' ')
    final_content = []
    for word in split_content:
        if word.isalpha():
            final_content.append(word)
    return " ".join(final_content)